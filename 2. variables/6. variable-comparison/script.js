// Numbers

let a = 1
let b = 2

console.log(a == b)
console.log(a > b)
console.log(a < b)
console.log(a <= b)
console.log(a >= b)

// Strings

let c = 'a'
let d = 'b'

console.log(c < d) //sorts things out alphabetically

// Boolean

let e = false
let f = false

console.log(e == f) //true

// Null and Undefined

let g = null
let h = undefined

console.log(g == h) //true
// They represent the exact same thing; lack of a value = lack of a value(true)